class Calculadora:

    @staticmethod
    def multiplicar(a: int, b: int) -> int :
        return a * b
    
    @staticmethod
    def dividir(a: int, b: int) -> int:
        return int (a / b)
    
    @staticmethod
    def sumar(a: int, b: int) -> int:
        return a + b
    
    @staticmethod
    def restar(a: int , b: int) -> int:
        return a - b